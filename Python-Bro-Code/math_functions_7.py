### Section 7: Math Functions

import math

pi = 3.14
x = 1
y = 2
z = 3

# round
print(round(pi))

# ceil
print(math.ceil(pi))

# floor
print(math.floor(pi))

# absolute
print(abs(-3.14))

# pow
print(pow(pi, 2))

# square root
print(math.sqrt(pi))

# max
print(max(x,y,z))

# min
print(min(x,y,z))